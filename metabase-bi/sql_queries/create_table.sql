CREATE TABLE supermarket_sales (
    InvoiceID VARCHAR(50),
    Branch VARCHAR(10),
    City VARCHAR(50),
    CustomerType VARCHAR(50),
    Gender VARCHAR(10),
    ProductLine VARCHAR(50),
    UnitPrice NUMERIC,
    Quantity INT,
    Tax NUMERIC,
    Total NUMERIC,
    Date DATE,
    Time TIME,
    Payment VARCHAR(50),
    COGS NUMERIC,
    GrossMarginPercentage NUMERIC,
    GrossIncome NUMERIC,
    Rating NUMERIC
);
